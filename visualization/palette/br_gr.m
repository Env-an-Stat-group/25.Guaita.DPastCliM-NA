function newmap = br_gr(m)
% BWR_TOAR   Brown, to green color map for TOAR special issue guidelines 2023/24.

if ~exist('m','var') 
    m = 8;
end


color_list = [...
    0,60,48;...
    14,114,106;...
    146,212,202;...
    233,242,241;...
    246,235,207;...
    215,177,105;...
    151,91,18;...
    84,48,5]/255;

newmap = interp1(linspace(0,1,size(color_list,1)),...
    color_list,...
    linspace(0,1,m));


end